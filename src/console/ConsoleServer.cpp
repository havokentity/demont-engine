namespace {
// Translate an errno-like int into a printable string. Winsock doesn't
// populate strerror() for socket errors -- WSAGetLastError() returns a
// numeric WSA* code that strerror() would mistranslate -- so on Windows
// we just print the number. Good enough for a log line.
inline std::string SockErrStr(int err) {
#if defined(_WIN32)
    return fmt::format("WSA error {}", err);
#else
    return std::strerror(err);
#endif
}
}  // namespace

