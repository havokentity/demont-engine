int ConsoleServer::WsDataHandler(mg_connection* conn, int op, char* data,
                                 std::size_t len, void* cbdata) {
    auto* self = static_cast<ConsoleServer*>(cbdata);
    (void)op;
    if ((op & 0xF) == MG_WEBSOCKET_OPCODE_CONNECTION_CLOSE) return 0;
    // Cap inbound frames. civetweb buffers the whole text frame before
    // this callback fires, and without a cap the payload would flow
    // wholesale into Console's exec queue and sit there until the next
    // Drain() -- one client could balloon memory far past anything the
    // engine budgets for. 1 MiB matches the bracket-batch cap in
    // Console::Drain(): same OOM class, same budget.
    constexpr std::size_t kMaxWsPayloadBytes = 1u * 1024u * 1024u;
    if (len > kMaxWsPayloadBytes) {
        LOG_WARN("WS message dropped: {} bytes exceeds {} byte cap",
                 len, kMaxWsPayloadBytes);
        self->SendWs(conn,
                     R"({"type":"result","ok":false,"error":"message too large"})");
        return 1;  // keep the connection; just drop the oversized frame
    }
    if (data != nullptr && len > 0) {
        self->HandleWsMessage(conn, std::string_view(data, len));
    }
    return 1;  // keep connection
}

void ConsoleServer::WsCloseHandler(const mg_connection* conn, void* cbdata) {
    auto* self = static_cast<ConsoleServer*>(cbdata);
    std::lock_guard lock(self->ws_mutex_);
    self->ws_clients_.erase(const_cast<mg_connection*>(conn));
}

void ConsoleServer::SendWs(mg_connection* conn, std::string_view text) {
    if (conn == nullptr) return;
    mg_websocket_write(conn, MG_WEBSOCKET_OPCODE_TEXT, text.data(),
                       text.size());
}

// ----- Message dispatch -----------------------------------------------------

void ConsoleServer::HandleWsMessage(mg_connection* conn, std::string_view payload) {
    json msg = json::parse(payload, nullptr, /*allow_exceptions=*/false);
    if (msg.is_discarded() || !msg.is_object()) {
        SendWs(conn, R"({"type":"result","ok":false,"error":"bad json"})");
        return;
    }

    // All field reads below use StrField (see anon namespace above):
    // msg.value<std::string>() aborts the process on a type-mismatched
    // field in this JSON_NOEXCEPTION build, and every byte here is
    // untrusted wire input. `id` in particular is a NUMBER in the
    // legacy select protocol (see the select handler below), so it
    // must tolerate non-string values by design.
    std::string type = StrField(msg, "type");
    std::string id   = StrField(msg, "id");

    auto reply = [&](const json& body) {
        json out = body;
        out["type"] = "result";
        if (!id.empty()) out["id"] = id;
        SendWs(conn, out.dump());
    };

    if (type == "exec") {
        std::string line = StrField(msg, "line");
        Console::Get().QueueExecute(std::move(line),
            [conn, id, this](const ExecuteResult& r) {
                json body{{"ok", r.ok}};
                if (!r.output.empty()) body["output"] = r.output;
                if (!r.ok) body["error"] = r.error;
                if (!id.empty()) body["id"] = id;
                body["type"] = "result";
                std::lock_guard lock(this->ws_mutex_);
                if (this->ws_clients_.contains(conn)) SendWs(conn, body.dump());
            });
        return;
    }

    if (type == "get_cvar") {
        // Snapshot on the MAIN thread: Execute() reassigns CVar::value
        // (a std::string) there, and reading it from this civetweb
        // worker races a reallocating string. Same reason exec /
        // set_cvar route their writes through the queue.
        std::string name = StrField(msg, "name");
        Console::Get().QueueTask([conn, id, name, this]() {
            json body;
            auto* v = Console::Get().FindCVar(name);
            if (v == nullptr) {
                body = json{{"ok", false},
                            {"error", fmt::format("unknown cvar: {}", name)}};
            } else {
                json cvarObj{
                    {"name",        v->name},
                    {"value",       v->value},
                    {"default",     v->default_value},
                    {"description", v->description},
                    {"flags",       v->flags},
                };
                if (!v->allowed_values.empty()) {
                    cvarObj["allowed_values"] = v->allowed_values;
                }
                if (v->slider_max > v->slider_min) {
                    cvarObj["slider_min"]  = v->slider_min;
                    cvarObj["slider_max"]  = v->slider_max;
                    cvarObj["slider_step"] = v->slider_step;
                }
                body = json{{"ok", true}, {"cvar", cvarObj}};
            }
            body["type"] = "result";
            if (!id.empty()) body["id"] = id;
            std::lock_guard lock(this->ws_mutex_);
            if (this->ws_clients_.contains(conn)) SendWs(conn, body.dump());
        });
        return;
    }

    if (type == "set_cvar") {
        std::string name  = StrField(msg, "name");
        std::string value = StrField(msg, "value");
        // `name` is concatenated into the console line UNQUOTED, so
        // gate it to identifier characters -- every registered cvar /
        // command name is a C identifier (PT_CVAR / PT_COMMAND
        // stringize variable names). Without this, a name like
        // "x;dev_cheats 1" would smuggle a second statement past the
        // single-cvar contract of set_cvar.
        const bool name_ok = !name.empty() &&
            std::all_of(name.begin(), name.end(), [](unsigned char c) {
                return std::isalnum(c) != 0 || c == '_';
            });
        if (!name_ok) {
            reply(json{{"ok", false},
                       {"error", fmt::format("bad cvar name: {}", name)}});
            return;
        }
        // QuoteForConsole escapes '\' and '"' (the exact inverse of
        // TokenizeLine's unescape), so a value containing a literal
        // double quote can't break out of the quoting -- previously
        // `a";dev_cheats 1;"` became `r_x "a";dev_cheats 1;""` and
        // the quote-aware ';' splitter in ExecuteScript ran the
        // injected statement.
        std::string line = fmt::format("{} {}", name, QuoteForConsole(value));
        Console::Get().QueueExecute(std::move(line),
            [conn, id, this](const ExecuteResult& r) {
                json body{{"ok", r.ok}};
                if (!r.ok) body["error"] = r.error;
                if (!r.output.empty()) body["output"] = r.output;
                if (!id.empty()) body["id"] = id;
                body["type"] = "result";
                std::lock_guard lock(this->ws_mutex_);
                if (this->ws_clients_.contains(conn)) SendWs(conn, body.dump());
            });
        return;
    }

    if (type == "list_cvars") {
        // Main-thread snapshot -- see the get_cvar comment. A panel
        // polling list_cvars while another drags a slider used to copy
        // CVar::value strings out from under Execute()'s reassignment.
        std::string prefix = StrField(msg, "prefix");
        Console::Get().QueueTask([conn, id, prefix, this]() {
            json arr = json::array();
            Console::Get().EnumerateCVars(prefix, [&arr](CVar& v) {
                json entry{
                    {"name",        v.name},
                    {"value",       v.value},
                    {"default",     v.default_value},
                    {"description", v.description},
                    {"flags",       v.flags},
                };
                if (!v.allowed_values.empty()) {
                    entry["allowed_values"] = v.allowed_values;
                }
                if (v.slider_max > v.slider_min) {
                    entry["slider_min"]  = v.slider_min;
                    entry["slider_max"]  = v.slider_max;
                    entry["slider_step"] = v.slider_step;
                }
                arr.push_back(entry);
            });
            json body{{"ok", true}, {"cvars", arr}};
            body["type"] = "result";
            if (!id.empty()) body["id"] = id;
            std::lock_guard lock(this->ws_mutex_);
            if (this->ws_clients_.contains(conn)) SendWs(conn, body.dump());
        });
        return;
    }

    if (type == "list_commands") {
        // Safe to read from this worker: commands_ is populated during
        // Engine::Init registration and never mutated afterwards (no
        // command registers/unregisters at runtime), unlike CVar::value.
        std::string prefix = StrField(msg, "prefix");
        json arr = json::array();
        Console::Get().EnumerateCommands(prefix, [&arr](Command& c) {
            json entry{
                {"name",        c.name},
                {"description", c.description},
            };
            if (!c.default_args.empty()) entry["default_args"] = c.default_args;
            arr.push_back(entry);
        });
        reply(json{{"ok", true}, {"commands", arr}});
        return;
    }

    // --- Editor backend (agent-19): list_scene + select dispatch ----------
    if (type == "list_scene") {
        // Editor scene-graph snapshot. The hook returns the full JSON
        // body so the server doesn't need to depend on pt_editor /
        // pt_engine -- only the engine knows how to populate it. When
        // the hook isn't installed we reply with an empty scene so the
        // client gets a deterministic shape rather than a parse error.
        json scene_obj;
        if (scene_dump_handler_) {
            const std::string body = scene_dump_handler_();
            scene_obj = json::parse(body, nullptr, /*allow_exceptions=*/false);
            if (scene_obj.is_discarded()) scene_obj = json::object();
        } else {
            scene_obj = json::object();
        }
        reply(json{{"ok", true}, {"scene", scene_obj}});
        return;
    }
    if (type == "select") {
        // Editor selection set. {"type":"select","kind":"prim","obj_id":N}.
        // The hook owns the SelectionKind mapping (the server doesn't
        // know about the engine's enum). The selection_change broadcast
        // is fired from the engine side AFTER it applies the mutation
        // so panels / native overlays that subscribe to that topic see
        // the same payload as a click-driven selection.
        //
        // The object id is delivered in `obj_id` so it doesn't collide
        // with the optional top-level `id` field that every inbound
        // message can carry as a request-id (echoed back in `result`
        // for client-side matching -- a string in the TS WS client).
        // For backward compat with older clients that put the object
        // id in `id` as a JSON number, we fall through to that field
        // when `obj_id` isn't present.
        std::string kind = StrField(msg, "kind");
        std::uint32_t sel_id = 0;
        if (msg.contains("obj_id") && msg["obj_id"].is_number_unsigned()) {
            sel_id = msg["obj_id"].get<std::uint32_t>();
        } else if (msg.contains("id") && msg["id"].is_number_unsigned()) {
            sel_id = msg["id"].get<std::uint32_t>();
        }
        if (select_handler_) select_handler_(kind, sel_id);
        reply(json{{"ok", true}});
        return;
    }
    // --- end Editor backend ----------------------------------------------

    if (type == "subscribe" || type == "unsubscribe") {
        std::vector<std::string> topics;
        if (msg.contains("topics") && msg["topics"].is_array()) {
            for (auto& t : msg["topics"]) {
                if (t.is_string()) topics.push_back(t.get<std::string>());
            }
        }
        std::lock_guard lock(ws_mutex_);
        auto it = ws_clients_.find(conn);
        if (it != ws_clients_.end()) {
            if (type == "subscribe") {
                for (auto& t : topics) it->second.topics.insert(t);
            } else {
                for (auto& t : topics) it->second.topics.erase(t);
            }
        }
        reply(json{{"ok", true}, {"subscribed", topics}});
        return;
    }

    reply(json{{"ok", false}, {"error", fmt::format("unknown type: {}", type)}});
}

void ConsoleServer::BroadcastEvent(std::string_view topic, std::string_view data_json) {
    auto evt = fmt::format(R"({{"type":"event","topic":"{}","ts":{:.3f},"data":{}}})",
                           topic, NowSeconds(), data_json);
    std::lock_guard lock(ws_mutex_);
    for (auto& [conn, client] : ws_clients_) {
        if (client.topics.contains(std::string(topic))) {
            SendWs(conn, evt);
        }
    }
}

void ConsoleServer::OnLog(pt::log::Level level, const std::string& body) {
    if (g_instance == nullptr) return;
    // Manually escape `body` for JSON.
    std::string escaped;
    escaped.reserve(body.size() + 8);
    for (char c : body) {
        switch (c) {
            case '"':  escaped += "\\\""; break;
            case '\\': escaped += "\\\\"; break;
            case '\b': escaped += "\\b";  break;
            case '\f': escaped += "\\f";  break;
            case '\n': escaped += "\\n";  break;
            case '\r': escaped += "\\r";  break;
            case '\t': escaped += "\\t";  break;
            default:
                if (static_cast<unsigned char>(c) < 0x20) {
                    escaped += fmt::format("\\u{:04x}",
                                           static_cast<unsigned char>(c));
                } else {
                    escaped.push_back(c);
                }
        }
    }
    auto data = fmt::format(R"({{"level":"{}","message":"{}"}})",
                            LevelName(level), escaped);
    g_instance->BroadcastEvent("log", data);
}

// ----- Line protocol --------------------------------------------------------

void ConsoleServer::LineAcceptLoop() {
    auto listen_s = static_cast<pt_sock_t>(line_fd_);
    while (line_run_) {
        sockaddr_in caddr{};
        pt_socklen clen = sizeof(caddr);
        pt_sock_t cfd = ::accept(listen_s,
                                 reinterpret_cast<sockaddr*>(&caddr), &clen);
        if (!pt_sock_valid(cfd)) {
            if (!line_run_) break;
            continue;
        }
        // Reap workers that finished since the last accept -- otherwise
        // each short-lived `nc` connection leaves a joinable
        // std::thread (stack mapping + kernel object) parked until
        // Stop(). Extract under the lock, join after releasing it (the
        // exiting worker needs the same mutex to publish itself).
        std::vector<std::thread> finished;
        {
            std::lock_guard lock(line_workers_mutex_);
            for (auto it = line_workers_.begin(); it != line_workers_.end();) {
                auto done = std::find(line_workers_done_.begin(),
                                      line_workers_done_.end(),
                                      it->get_id());
                if (done != line_workers_done_.end()) {
                    finished.push_back(std::move(*it));
                    line_workers_done_.erase(done);
                    it = line_workers_.erase(it);
                } else {
                    ++it;
                }
            }
            line_workers_.emplace_back(&ConsoleServer::LineClientLoop, this,
                                       static_cast<socket_handle_t>(cfd));
        }
        for (auto& t : finished) {
            if (t.joinable()) t.join();
        }
    }
}

void ConsoleServer::LineClientLoop(socket_handle_t fd_) {
    auto fd = static_cast<pt_sock_t>(fd_);
    // Register with the live-client set so Stop() can ::shutdown() this
    // socket and break us out of the blocking recv() below -- closing
    // the LISTEN socket alone doesn't wake accepted clients, and the
    // join in Stop() would otherwise hang on an idle connection.
    {
        std::lock_guard lock(line_clients_mutex_);
        line_clients_.insert(fd_);
    }
    std::string buf;
    buf.reserve(256);
    char chunk[256];
    while (line_run_) {
        // Winsock recv() takes int for length and returns int; POSIX uses
        // size_t / ssize_t. Cast through pt_ssize_t to keep both compilers
        // happy without -Wnarrowing complaints.
        pt_ssize_t n = ::recv(fd, chunk,
                              static_cast<int>(sizeof(chunk)), 0);
        if (n <= 0) break;
        buf.append(chunk, chunk + n);

        std::size_t start = 0;
        for (std::size_t i = 0; i < buf.size(); ++i) {
            if (buf[i] == '\n') {
                auto line = buf.substr(start, i - start);
                if (!line.empty() && line.back() == '\r') line.pop_back();
                start = i + 1;
                if (line.empty()) continue;

                // Synchronously execute on the main-thread queue and
                // reply. The rendezvous state is heap-shared with the
                // responder: Console::Drain() may run long AFTER the
                // 2 s wait below gives up (cold pipeline builds and
                // heavy scene loads block the main thread for far
                // longer), and the queued responder fires whenever
                // Drain finally gets to it. Capturing stack locals by
                // reference here would have that late call writing
                // through dangling pointers -- a destroyed mutex and
                // ExecuteResult, or a torn-down thread stack if the
                // client disconnected. The shared_ptr keeps the state
                // alive for however late the responder fires; a
                // timed-out client simply never sees the reply.
                struct SyncState {
                    std::mutex              m;
                    std::condition_variable cv;
                    bool                    done = false;
                    ExecuteResult           result;
                };
                auto st = std::make_shared<SyncState>();
                Console::Get().QueueExecute(line,
                    [st](const ExecuteResult& r) {
                        std::lock_guard lock(st->m);
                        st->result = r;
                        st->done = true;
                        st->cv.notify_one();
                    });
                bool done = false;
                ExecuteResult result;
                {
                    std::unique_lock lock(st->m);
                    st->cv.wait_for(lock, std::chrono::seconds(2),
                                    [&]{ return st->done; });
                    done = st->done;
                    if (done) result = st->result;
                }
                if (done) {
                    if (result.ok) {
                        if (!result.output.empty()) {
                            ::send(fd, result.output.data(),
                                   static_cast<int>(result.output.size()), 0);
                            if (result.output.back() != '\n') {
                                ::send(fd, "\n", 1, 0);
                            }
                        }
                    } else {
                        std::string msg = "error: " + result.error + "\n";
                        ::send(fd, msg.data(),
                               static_cast<int>(msg.size()), 0);
                    }
                }
            }
        }
        if (start > 0) buf.erase(0, start);

        // Per-line cap on the residual (no '\n' seen yet). A client
        // that streams bytes without ever sending a newline would
        // otherwise grow `buf` until allocation failure kills the
        // process -- the same OOM class the bracket-batch path in
        // Console::Drain() caps at 1 MiB, but one transport layer
        // below it. 64 KiB dwarfs any sane console line (the largest
        // replay scripts in this codebase are batched line-by-line),
        // so tripping it means a broken or hostile client: tell it
        // why, then drop the connection.
        constexpr std::size_t kMaxLineBytes = 64u * 1024u;
        if (buf.size() > kMaxLineBytes) {
            LOG_WARN("line client dropped: unterminated line exceeds "
                     "{} byte cap", kMaxLineBytes);
            const char err[] = "error: line too long\n";
            ::send(fd, err, static_cast<int>(sizeof(err) - 1), 0);
            break;
        }
    }
    {
        std::lock_guard lock(line_clients_mutex_);
        line_clients_.erase(fd_);
    }
    PT_SOCK_CLOSE(fd);
    {
        // Publish self as reapable; the next accept (or Stop) joins us.
        std::lock_guard lock(line_workers_mutex_);
        line_workers_done_.push_back(std::this_thread::get_id());
    }
}

