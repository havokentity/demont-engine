bool ConsoleServer::Start(const Config& cfg, Console* console) {
    if (ctx_ != nullptr) return false;
    config_  = cfg;
    console_ = console;

#if defined(_WIN32)
    // Winsock requires per-process init before any socket() / bind() call.
    // civetweb does its own WSAStartup internally, but our line-protocol
    // listener is independent so we initialise here and pair with a
    // matching WSACleanup() in Stop() (or in any failure-path unwind
    // below).  Track success so a later mg_start() failure can pair
    // each init with its matching cleanup -- otherwise repeated
    // Start() retries leak process-wide socket / library state.
    bool wsa_started = false;
    {
        WSADATA wsa{};
        int wsa_err = WSAStartup(MAKEWORD(2, 2), &wsa);
        if (wsa_err != 0) {
            LOG_ERROR("WSAStartup failed: {}", wsa_err);
            // Carry on -- HTTP via civetweb may still work.
        } else {
            wsa_started = true;
        }
    }
#endif

    mg_init_library(0);
    bool mg_lib_initialised = true;

    auto port_spec = fmt::format("{}:{}", config_.bind_address, config_.http_port);
    const char* opts[] = {
        "listening_ports",      port_spec.c_str(),
        "num_threads",          "8",
        "decode_url",           "no",
        "enable_keep_alive",    "yes",
        "websocket_timeout_ms", "3600000",
        nullptr,
    };

    mg_callbacks callbacks{};
    ctx_ = mg_start(&callbacks, this, opts);
    if (ctx_ == nullptr) {
        LOG_ERROR("Failed to start civetweb on {}", port_spec);
        // Unwind in reverse init order so process-wide library state
        // doesn't leak across retried Start() calls.
        if (mg_lib_initialised) {
            mg_exit_library();
        }
#if defined(_WIN32)
        if (wsa_started) {
            WSACleanup();
        }
#endif
        return false;
    }

    // HTTP routes: serve embedded UI.
    mg_set_request_handler(ctx_, "/", &ConsoleServer::HttpHandler, this);

    // WebSocket on root path.
    mg_set_websocket_handler(ctx_, "/ws",
                             &ConsoleServer::WsConnectHandler,
                             &ConsoleServer::WsReadyHandler,
                             &ConsoleServer::WsDataHandler,
                             &ConsoleServer::WsCloseHandler,
                             this);

    // Line protocol on second port.
    sockaddr_in addr{};
    addr.sin_family = AF_INET;
    addr.sin_port   = htons(config_.line_port);
    if (inet_pton(AF_INET, config_.bind_address.c_str(), &addr.sin_addr) <= 0) {
        addr.sin_addr.s_addr = htonl(INADDR_LOOPBACK);
    }

    pt_sock_t s = ::socket(AF_INET, SOCK_STREAM, 0);
    if (!pt_sock_valid(s)) {
        LOG_ERROR("line socket(): {}", SockErrStr(PT_SOCK_LASTERR));
        line_fd_ = kInvalidSocket;
        return true;  // HTTP is up; carry on without line protocol
    }
    int reuse = 1;
    setsockopt(s, SOL_SOCKET, SO_REUSEADDR,
               reinterpret_cast<const char*>(&reuse), sizeof(reuse));
    if (::bind(s, reinterpret_cast<sockaddr*>(&addr), sizeof(addr)) < 0) {
        LOG_ERROR("line bind {}:{}: {}", config_.bind_address,
                  config_.line_port, SockErrStr(PT_SOCK_LASTERR));
        PT_SOCK_CLOSE(s);
        line_fd_ = kInvalidSocket;
    } else if (::listen(s, 8) < 0) {
        LOG_ERROR("line listen: {}", SockErrStr(PT_SOCK_LASTERR));
        PT_SOCK_CLOSE(s);
        line_fd_ = kInvalidSocket;
    } else {
        line_fd_ = static_cast<socket_handle_t>(s);
        line_run_ = true;
        line_thread_ = std::thread(&ConsoleServer::LineAcceptLoop, this);
    }

    // Banner spacers stay on plain LOG_INFO so they're truly blank
    // instead of "[console] " with a trailing space. Only the content
    // lines tier-gate; per Copilot review on PR #6.
    LOG_INFO("");
    PT_DIAG_TIER1("console", "---- console listening ----");
    PT_DIAG_TIER1("console", "  Web UI    http://{}:{}/", config_.bind_address, config_.http_port);
    PT_DIAG_TIER1("console", "  WebSocket ws://{}:{}/ws",  config_.bind_address, config_.http_port);
    PT_DIAG_TIER1("console", "  Line TCP  {}:{}",           config_.bind_address, config_.line_port);
    PT_DIAG_TIER1("console", "  Auth      disabled");
    PT_DIAG_TIER1("console", "---------------------------");
    LOG_INFO("");

    return true;
}

void ConsoleServer::Stop() {
    line_run_ = false;
    if (line_fd_ != kInvalidSocket) {
        auto s = static_cast<pt_sock_t>(line_fd_);
        ::shutdown(s, PT_SOCK_SHUTDOWN_BOTH);
        PT_SOCK_CLOSE(s);
        line_fd_ = kInvalidSocket;
    }
    // Wake every per-client worker parked in a blocking recv().
    // Closing the listen socket above only unblocks the accept loop;
    // accepted client sockets are independent, so a connected-but-
    // idle client would otherwise hold its worker in recv() forever
    // and the joins below would hang engine shutdown. shutdown() --
    // not close() -- so the worker still owns the fd's lifetime and
    // closes it on loop exit as usual. Workers racing between accept
    // and set-insertion are safe too: they re-check line_run_ (now
    // false) before their first recv().
    {
        std::lock_guard lock(line_clients_mutex_);
        for (socket_handle_t cfd : line_clients_) {
            ::shutdown(static_cast<pt_sock_t>(cfd), PT_SOCK_SHUTDOWN_BOTH);
        }
    }
    if (line_thread_.joinable()) line_thread_.join();
    // Move the worker handles out under the lock, then join OUTSIDE it:
    // an exiting worker takes line_workers_mutex_ to publish itself as
    // reapable, so joining while holding it would deadlock.
    std::vector<std::thread> to_join;
    {
        std::lock_guard workers_lock(line_workers_mutex_);
        to_join.swap(line_workers_);
        line_workers_done_.clear();
    }
    for (auto& t : to_join) {
        if (t.joinable()) t.join();
    }

    if (ctx_ != nullptr) {
        mg_stop(ctx_);
        ctx_ = nullptr;
        mg_exit_library();
    }

#if defined(_WIN32)
    // Pair the WSAStartup() in Start(). Safe to call even if startup
    // failed; Winsock just returns an error which we ignore.
    WSACleanup();
#endif
}

// ----- HTTP -----------------------------------------------------------------

int ConsoleServer::HttpHandler(mg_connection* conn, void* cbdata) {
    auto* self = static_cast<ConsoleServer*>(cbdata);
    (void)self;
    const auto* req = mg_get_request_info(conn);
    std::string_view uri = req->local_uri ? req->local_uri : "/";
    if (uri == "/" || uri == "/index.html") {
        SendStaticHttp(conn, "text/html; charset=utf-8",
                       web_index_html_data, web_index_html_size);
        return 200;
    }
    if (uri == "/console.js") {
        SendStaticHttp(conn, "application/javascript",
                       web_console_js_data, web_console_js_size);
        return 200;
    }
    if (uri == "/console.css") {
        SendStaticHttp(conn, "text/css",
                       web_console_css_data, web_console_css_size);
        return 200;
    }

    // Editor routes (React+Vite multi-panel). pt::editor::ResolveRoute
    // handles three cases internally:
    //   - `/editor`                synthesised panel index
    //   - `/editor/<panel>`        rewrites to the panel's index.html
    //   - `/panels/<...>`          direct lookup in the embedded table
    //   - `/shared/<...>`          ditto
    // A miss returns nullptr -> 404 fall-through.
    if (uri == "/editor" || uri.starts_with("/editor/") ||
        uri.starts_with("/panels/") || uri.starts_with("/shared/")) {
        if (const auto* asset = pt::editor::ResolveRoute(uri)) {
            SendStaticHttp(conn, asset->mime, asset->data, asset->size);
            return 200;
        }
    }

    mg_send_http_error(conn, 404, "%s", "not found");
    return 404;
}

// ----- WebSocket ------------------------------------------------------------

int ConsoleServer::WsConnectHandler(const mg_connection* conn, void* cbdata) {
    auto* self = static_cast<ConsoleServer*>(cbdata);
    const auto* req = mg_get_request_info(conn);

    // Origin check: same-origin, absent (raw clients), or the Vite dev
    // servers. In dev mode (r_editor_dev_mode) the panels are served
    // from Vite on :5173 (dev) / :5174 (preview) -- see
    // web/editor/vite.config.ts (strictPort) -- but their WebSocket
    // still targets the engine's HTTP port directly (endpoint.ts), so
    // the handshake arrives with a cross-origin Origin header.
    //
    // A browser's Origin carries the host the user DIALED, never the
    // address we bound: pages served by the engine build their WS URL
    // from location.host (console.js / endpoint.ts). So the primary
    // same-origin test compares Origin against "http://" + the
    // request's own Host header -- that's what keeps non-loopback
    // binds working (net_bind_address=0.0.0.0 never appears in a real
    // Origin, and a LAN client dials the machine's LAN IP). The
    // literal spellings below back that up: both loopback names for
    // the engine port (localhost vs 127.0.0.1 are distinct origins to
    // a browser), the configured bind_address form for explicit
    // single-IP binds, and the Vite dev/preview pairs.
    if (req != nullptr) {
        const char* origin_hdr = nullptr;
        const char* host_hdr   = nullptr;
        for (int i = 0; i < req->num_headers; ++i) {
            if (PT_STRCASECMP(req->http_headers[i].name, "Origin") == 0) {
                origin_hdr = req->http_headers[i].value;
            } else if (PT_STRCASECMP(req->http_headers[i].name, "Host") == 0) {
                host_hdr = req->http_headers[i].value;
            }
        }
        if (origin_hdr != nullptr) {
            const std::string origin = origin_hdr;
            // Fixed allowlist of the host:port spellings we'll ever
            // legitimately be dialed as. Both loopback names (localhost
            // and 127.0.0.1 are distinct origins to a browser), the
            // configured bind_address for an explicit single-IP bind,
            // and the Vite dev/preview pairs.
            const std::array<std::string, 7> allowed = {
                fmt::format("http://localhost:{}", self->config_.http_port),
                fmt::format("http://127.0.0.1:{}", self->config_.http_port),
                // Inert when bind_address is 0.0.0.0 / a loopback
                // duplicate; matches when the user bound one explicit
                // LAN IP.
                fmt::format("http://{}:{}",
                    self->config_.bind_address, self->config_.http_port),
                std::string("http://localhost:5173"),
                std::string("http://localhost:5174"),
                std::string("http://127.0.0.1:5173"),
                std::string("http://127.0.0.1:5174"),
            };
            auto in_allowlist = [&](const std::string& s) {
                return std::find(allowed.begin(), allowed.end(), s)
                       != allowed.end();
            };

            // Validate the HOST header against the allowlist FIRST. The
            // same-origin fast-path below compares Origin to Host, but
            // Host is attacker-controlled -- a DNS-rebinding page
            // (evil.com rebound to 127.0.0.1) sends matching
            // Origin: http://evil.com:PORT and Host: evil.com:PORT,
            // which a bare Origin==Host test accepts, handing a
            // malicious web page full unauthenticated console/exec
            // access on a loopback-bound engine. Rejecting unknown
            // Hosts kills rebinding on both paths.
            const bool host_ok = (host_hdr != nullptr && *host_hdr != '\0')
                                 && in_allowlist(fmt::format("http://{}", host_hdr));

            bool ok = false;
            if (host_ok) {
                // Same-origin: Origin equals the (now-vetted) Host.
                ok = (origin == fmt::format("http://{}", host_hdr));
            }
            if (!ok) {
                // Cross-origin but explicitly allowed (Vite dev panels
                // dialing the engine port; a client on one loopback
                // spelling served from the other).
                ok = in_allowlist(origin);
            }
            if (!ok) {
                LOG_WARN("WS rejected: bad Origin '{}' (Host '{}')",
                         origin, host_hdr ? host_hdr : "");
                return 1;  // reject
            }
        }
    }

    std::lock_guard lock(self->ws_mutex_);
    self->ws_clients_[const_cast<mg_connection*>(conn)] = WsClient{
        const_cast<mg_connection*>(conn), {}
    };
    return 0;  // accept
}

