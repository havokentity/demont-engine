ConsoleServer::~ConsoleServer() { Stop(); }

void ConsoleServer::SetGlobalInstance(ConsoleServer* s) { g_instance = s; }

bool ConsoleServer::Start(const Config& cfg, Console* console) {
    if (ctx_ != nullptr) return false;
    config_  = cfg;
    console_ = console;

#if defined(_WIN32)
    // Winsock requires per-process init before any socket() / bind() call.
    // civetweb does its own WSAStartup internally, but our line-protocol
    // listener is independent so we initialise here and pair with a
    // matching WSACleanup() in Stop() (or in any failure-path unwind
    // below).  Track success so a later mg_start() failure can pair
    // each init with its matching cleanup -- otherwise repeated
    // Start() retries leak process-wide socket / library state.
    bool wsa_started = false;
    {
        WSADATA wsa{};
        int wsa_err = WSAStartup(MAKEWORD(2, 2), &wsa);
        if (wsa_err != 0) {
            LOG_ERROR("WSAStartup failed: {}", wsa_err);
            // Carry on -- HTTP via civetweb may still work.
        } else {
            wsa_started = true;
        }
    }
#endif

    mg_init_library(0);
    bool mg_lib_initialised = true;

    auto port_spec = fmt::format("{}:{}", config_.bind_address, config_.http_port);
    const char* opts[] = {
        "listening_ports",      port_spec.c_str(),
        "num_threads",          "8",
        "decode_url",           "no",
        "enable_keep_alive",    "yes",
        "websocket_timeout_ms", "3600000",
        nullptr,
    };

    mg_callbacks callbacks{};
    ctx_ = mg_start(&callbacks, this, opts);
    if (ctx_ == nullptr) {
        LOG_ERROR("Failed to start civetweb on {}", port_spec);
        // Unwind in reverse init order so process-wide library state
        // doesn't leak across retried Start() calls.
        if (mg_lib_initialised) {
            mg_exit_library();
        }
#if defined(_WIN32)
        if (wsa_started) {
            WSACleanup();
        }
#endif
        return false;
    }

    // HTTP routes: serve embedded UI.
    mg_set_request_handler(ctx_, "/", &ConsoleServer::HttpHandler, this);

    // WebSocket on root path.
    mg_set_websocket_handler(ctx_, "/ws",
                             &ConsoleServer::WsConnectHandler,
                             &ConsoleServer::WsReadyHandler,
                             &ConsoleServer::WsDataHandler,
                             &ConsoleServer::WsCloseHandler,
                             this);

    // Line protocol on second port.
    sockaddr_in addr{};
    addr.sin_family = AF_INET;
    addr.sin_port   = htons(config_.line_port);
    if (inet_pton(AF_INET, config_.bind_address.c_str(), &addr.sin_addr) <= 0) {
        addr.sin_addr.s_addr = htonl(INADDR_LOOPBACK);
    }

    pt_sock_t s = ::socket(AF_INET, SOCK_STREAM, 0);
    if (!pt_sock_valid(s)) {
        LOG_ERROR("line socket(): {}", SockErrStr(PT_SOCK_LASTERR));
        line_fd_ = kInvalidSocket;
        return true;  // HTTP is up; carry on without line protocol
    }
    int reuse = 1;
    setsockopt(s, SOL_SOCKET, SO_REUSEADDR,
               reinterpret_cast<const char*>(&reuse), sizeof(reuse));
    if (::bind(s, reinterpret_cast<sockaddr*>(&addr), sizeof(addr)) < 0) {
        LOG_ERROR("line bind {}:{}: {}", config_.bind_address,
                  config_.line_port, SockErrStr(PT_SOCK_LASTERR));
        PT_SOCK_CLOSE(s);
        line_fd_ = kInvalidSocket;
    } else if (::listen(s, 8) < 0) {
        LOG_ERROR("line listen: {}", SockErrStr(PT_SOCK_LASTERR));
        PT_SOCK_CLOSE(s);
        line_fd_ = kInvalidSocket;
    } else {
        line_fd_ = static_cast<socket_handle_t>(s);
        line_run_ = true;
        line_thread_ = std::thread(&ConsoleServer::LineAcceptLoop, this);
    }

    // Banner spacers stay on plain LOG_INFO so they're truly blank
    // instead of "[console] " with a trailing space. Only the content
    // lines tier-gate; per Copilot review on PR #6.
    LOG_INFO("");
    PT_DIAG_TIER1("console", "---- console listening ----");
    PT_DIAG_TIER1("console", "  Web UI    http://{}:{}/", config_.bind_address, config_.http_port);
    PT_DIAG_TIER1("console", "  WebSocket ws://{}:{}/ws",  config_.bind_address, config_.http_port);
    PT_DIAG_TIER1("console", "  Line TCP  {}:{}",           config_.bind_address, config_.line_port);
    PT_DIAG_TIER1("console", "  Auth      disabled");
    PT_DIAG_TIER1("console", "---------------------------");
    LOG_INFO("");

    return true;
}

void ConsoleServer::Stop() {
    line_run_ = false;
    if (line_fd_ != kInvalidSocket) {
        auto s = static_cast<pt_sock_t>(line_fd_);
        ::shutdown(s, PT_SOCK_SHUTDOWN_BOTH);
        PT_SOCK_CLOSE(s);
        line_fd_ = kInvalidSocket;
    }
    if (line_thread_.joinable()) line_thread_.join();
    for (auto& t : line_workers_) {
        if (t.joinable()) t.join();
    }
    line_workers_.clear();

    if (ctx_ != nullptr) {
        mg_stop(ctx_);
        ctx_ = nullptr;
        mg_exit_library();
    }

#if defined(_WIN32)
    // Pair the WSAStartup() in Start(). Safe to call even if startup
    // failed; Winsock just returns an error which we ignore.
    WSACleanup();
#endif
}

// ----- HTTP -----------------------------------------------------------------

int ConsoleServer::HttpHandler(mg_connection* conn, void* cbdata) {
    auto* self = static_cast<ConsoleServer*>(cbdata);
    (void)self;
    const auto* req = mg_get_request_info(conn);
    std::string_view uri = req->local_uri ? req->local_uri : "/";
    if (uri == "/" || uri == "/index.html") {
        SendStaticHttp(conn, "text/html; charset=utf-8",
                       web_index_html_data, web_index_html_size);
        return 200;
    }
    if (uri == "/console.js") {
        SendStaticHttp(conn, "application/javascript",
                       web_console_js_data, web_console_js_size);
        return 200;
    }
    if (uri == "/console.css") {
        SendStaticHttp(conn, "text/css",
                       web_console_css_data, web_console_css_size);
        return 200;
    }

    // Editor routes (React+Vite multi-panel). pt::editor::ResolveRoute
    // handles three cases internally:
    //   - `/editor`                synthesised panel index
    //   - `/editor/<panel>`        rewrites to the panel's index.html
    //   - `/panels/<...>`          direct lookup in the embedded table
    //   - `/shared/<...>`          ditto
    // A miss returns nullptr -> 404 fall-through.
    if (uri == "/editor" || uri.starts_with("/editor/") ||
        uri.starts_with("/panels/") || uri.starts_with("/shared/")) {
        if (const auto* asset = pt::editor::ResolveRoute(uri)) {
            SendStaticHttp(conn, asset->mime, asset->data, asset->size);
            return 200;
        }
    }

    mg_send_http_error(conn, 404, "%s", "not found");
    return 404;
}

// ----- WebSocket ------------------------------------------------------------

int ConsoleServer::WsConnectHandler(const mg_connection* conn, void* cbdata) {
    auto* self = static_cast<ConsoleServer*>(cbdata);
    const auto* req = mg_get_request_info(conn);

    // Origin check: same-origin or absent (raw clients).
    if (req != nullptr) {
        for (int i = 0; i < req->num_headers; ++i) {
            if (PT_STRCASECMP(req->http_headers[i].name, "Origin") == 0) {
                std::string origin = req->http_headers[i].value
                                     ? req->http_headers[i].value : "";
                std::string ok1 = fmt::format("http://{}:{}",
                    self->config_.bind_address, self->config_.http_port);
                std::string ok2 = fmt::format("http://localhost:{}",
                    self->config_.http_port);
                if (origin != ok1 && origin != ok2) {
                    LOG_WARN("WS rejected: bad Origin '{}'", origin);
                    return 1;  // reject
                }
                break;
            }
        }
    }

    std::lock_guard lock(self->ws_mutex_);
    self->ws_clients_[const_cast<mg_connection*>(conn)] = WsClient{
        const_cast<mg_connection*>(conn), {}
    };
    return 0;  // accept
}

void ConsoleServer::WsReadyHandler(mg_connection* /*conn*/, void* /*cbdata*/) {
    // Could send a hello here. For now the client subscribes itself.
}

int ConsoleServer::WsDataHandler(mg_connection* conn, int op, char* data,
                                 std::size_t len, void* cbdata) {
    auto* self = static_cast<ConsoleServer*>(cbdata);
    (void)op;
    if ((op & 0xF) == MG_WEBSOCKET_OPCODE_CONNECTION_CLOSE) return 0;
    if (data != nullptr && len > 0) {
        self->HandleWsMessage(conn, std::string_view(data, len));
    }
    return 1;  // keep connection
}

void ConsoleServer::WsCloseHandler(const mg_connection* conn, void* cbdata) {
    auto* self = static_cast<ConsoleServer*>(cbdata);
    std::lock_guard lock(self->ws_mutex_);
    self->ws_clients_.erase(const_cast<mg_connection*>(conn));
}

void ConsoleServer::SendWs(mg_connection* conn, std::string_view text) {
    if (conn == nullptr) return;
    mg_websocket_write(conn, MG_WEBSOCKET_OPCODE_TEXT, text.data(),
                       text.size());
}

// ----- Message dispatch -----------------------------------------------------

void ConsoleServer::HandleWsMessage(mg_connection* conn, std::string_view payload) {
    json msg = json::parse(payload, nullptr, /*allow_exceptions=*/false);
    if (msg.is_discarded() || !msg.is_object()) {
        SendWs(conn, R"({"type":"result","ok":false,"error":"bad json"})");
        return;
    }

    std::string type = msg.value("type", std::string{});
    std::string id   = msg.value("id",   std::string{});

    auto reply = [&](const json& body) {
        json out = body;
        out["type"] = "result";
        if (!id.empty()) out["id"] = id;
        SendWs(conn, out.dump());
    };

    if (type == "exec") {
        std::string line = msg.value("line", std::string{});
        Console::Get().QueueExecute(std::move(line),
            [conn, id, this](const ExecuteResult& r) {
                json body{{"ok", r.ok}};
                if (!r.output.empty()) body["output"] = r.output;
                if (!r.ok) body["error"] = r.error;
                if (!id.empty()) body["id"] = id;
                body["type"] = "result";
                std::lock_guard lock(this->ws_mutex_);
                if (this->ws_clients_.contains(conn)) SendWs(conn, body.dump());
            });
        return;
    }

    if (type == "get_cvar") {
        std::string name = msg.value("name", std::string{});
        auto* v = Console::Get().FindCVar(name);
        if (v == nullptr) {
            reply(json{{"ok", false},
                       {"error", fmt::format("unknown cvar: {}", name)}});
            return;
        }
        json cvarObj{
            {"name",        v->name},
            {"value",       v->value},
            {"default",     v->default_value},
            {"description", v->description},
            {"flags",       v->flags},
        };
        if (!v->allowed_values.empty()) {
            cvarObj["allowed_values"] = v->allowed_values;
        }
        if (v->slider_max > v->slider_min) {
            cvarObj["slider_min"]  = v->slider_min;
            cvarObj["slider_max"]  = v->slider_max;
            cvarObj["slider_step"] = v->slider_step;
        }
        reply(json{{"ok", true}, {"cvar", cvarObj}});
        return;
    }

    if (type == "set_cvar") {
        std::string name  = msg.value("name",  std::string{});
        std::string value = msg.value("value", std::string{});
        std::string line  = fmt::format("{} \"{}\"", name, value);
        Console::Get().QueueExecute(std::move(line),
            [conn, id, this](const ExecuteResult& r) {
                json body{{"ok", r.ok}};
                if (!r.ok) body["error"] = r.error;
                if (!r.output.empty()) body["output"] = r.output;
                if (!id.empty()) body["id"] = id;
                body["type"] = "result";
                std::lock_guard lock(this->ws_mutex_);
                if (this->ws_clients_.contains(conn)) SendWs(conn, body.dump());
            });
        return;
    }

    if (type == "list_cvars") {
        std::string prefix = msg.value("prefix", std::string{});
        json arr = json::array();
        Console::Get().EnumerateCVars(prefix, [&arr](CVar& v) {
            json entry{
                {"name",        v.name},
                {"value",       v.value},
                {"default",     v.default_value},
                {"description", v.description},
                {"flags",       v.flags},
            };
            if (!v.allowed_values.empty()) {
                entry["allowed_values"] = v.allowed_values;
            }
            if (v.slider_max > v.slider_min) {
                entry["slider_min"]  = v.slider_min;
                entry["slider_max"]  = v.slider_max;
                entry["slider_step"] = v.slider_step;
            }
            arr.push_back(entry);
        });
        reply(json{{"ok", true}, {"cvars", arr}});
        return;
    }

    if (type == "list_commands") {
        std::string prefix = msg.value("prefix", std::string{});
        json arr = json::array();
        Console::Get().EnumerateCommands(prefix, [&arr](Command& c) {
            json entry{
                {"name",        c.name},
                {"description", c.description},
            };
            if (!c.default_args.empty()) entry["default_args"] = c.default_args;
            arr.push_back(entry);
        });
        reply(json{{"ok", true}, {"commands", arr}});
        return;
    }

    // --- Editor backend (agent-19): list_scene + select dispatch ----------
    if (type == "list_scene") {
        // Editor scene-graph snapshot. The hook returns the full JSON
        // body so the server doesn't need to depend on pt_editor /
        // pt_engine -- only the engine knows how to populate it. When
        // the hook isn't installed we reply with an empty scene so the
        // client gets a deterministic shape rather than a parse error.
        json scene_obj;
        if (scene_dump_handler_) {
            const std::string body = scene_dump_handler_();
            scene_obj = json::parse(body, nullptr, /*allow_exceptions=*/false);
            if (scene_obj.is_discarded()) scene_obj = json::object();
        } else {
            scene_obj = json::object();
        }
        reply(json{{"ok", true}, {"scene", scene_obj}});
        return;
    }
    if (type == "select") {
        // Editor selection set. {"type":"select","kind":"prim","obj_id":N}.
        // The hook owns the SelectionKind mapping (the server doesn't
        // know about the engine's enum). The selection_change broadcast
        // is fired from the engine side AFTER it applies the mutation
        // so panels / native overlays that subscribe to that topic see
        // the same payload as a click-driven selection.
        //
        // The object id is delivered in `obj_id` so it doesn't collide
        // with the optional top-level `id` field that every inbound
        // message can carry as a request-id (echoed back in `result`
        // for client-side matching -- a string in the TS WS client).
        // For backward compat with older clients that put the object
        // id in `id` as a JSON number, we fall through to that field
        // when `obj_id` isn't present.
        std::string kind = msg.value("kind", std::string{});
        std::uint32_t sel_id = 0;
        if (msg.contains("obj_id") && msg["obj_id"].is_number_unsigned()) {
            sel_id = msg["obj_id"].get<std::uint32_t>();
        } else if (msg.contains("id") && msg["id"].is_number_unsigned()) {
            sel_id = msg["id"].get<std::uint32_t>();
        }
        if (select_handler_) select_handler_(kind, sel_id);
        reply(json{{"ok", true}});
        return;
    }
    // --- end Editor backend ----------------------------------------------

    if (type == "subscribe" || type == "unsubscribe") {
        std::vector<std::string> topics;
        if (msg.contains("topics") && msg["topics"].is_array()) {
            for (auto& t : msg["topics"]) {
                if (t.is_string()) topics.push_back(t.get<std::string>());
            }
        }
        std::lock_guard lock(ws_mutex_);
        auto it = ws_clients_.find(conn);
        if (it != ws_clients_.end()) {
            if (type == "subscribe") {
                for (auto& t : topics) it->second.topics.insert(t);
            } else {
                for (auto& t : topics) it->second.topics.erase(t);
            }
        }
        reply(json{{"ok", true}, {"subscribed", topics}});
        return;
    }

    reply(json{{"ok", false}, {"error", fmt::format("unknown type: {}", type)}});
}

void ConsoleServer::BroadcastEvent(std::string_view topic, std::string_view data_json) {
    auto evt = fmt::format(R"({{"type":"event","topic":"{}","ts":{:.3f},"data":{}}})",
                           topic, NowSeconds(), data_json);
    std::lock_guard lock(ws_mutex_);
    for (auto& [conn, client] : ws_clients_) {
        if (client.topics.contains(std::string(topic))) {
            SendWs(conn, evt);
        }
    }
}

void ConsoleServer::OnLog(pt::log::Level level, const std::string& body) {
    if (g_instance == nullptr) return;
    // Manually escape `body` for JSON.
    std::string escaped;
    escaped.reserve(body.size() + 8);
    for (char c : body) {
        switch (c) {
            case '"':  escaped += "\\\""; break;
            case '\\': escaped += "\\\\"; break;
            case '\b': escaped += "\\b";  break;
            case '\f': escaped += "\\f";  break;
            case '\n': escaped += "\\n";  break;
            case '\r': escaped += "\\r";  break;
            case '\t': escaped += "\\t";  break;
            default:
                if (static_cast<unsigned char>(c) < 0x20) {
                    escaped += fmt::format("\\u{:04x}",
                                           static_cast<unsigned char>(c));
                } else {
                    escaped.push_back(c);
                }
        }
    }
    auto data = fmt::format(R"({{"level":"{}","message":"{}"}})",
                            LevelName(level), escaped);
    g_instance->BroadcastEvent("log", data);
}

// ----- Line protocol --------------------------------------------------------

void ConsoleServer::LineAcceptLoop() {
    auto listen_s = static_cast<pt_sock_t>(line_fd_);
    while (line_run_) {
        sockaddr_in caddr{};
        pt_socklen clen = sizeof(caddr);
        pt_sock_t cfd = ::accept(listen_s,
                                 reinterpret_cast<sockaddr*>(&caddr), &clen);
        if (!pt_sock_valid(cfd)) {
            if (!line_run_) break;
            continue;
        }
        line_workers_.emplace_back(&ConsoleServer::LineClientLoop, this,
                                   static_cast<socket_handle_t>(cfd));
    }
}

void ConsoleServer::LineClientLoop(socket_handle_t fd_) {
    auto fd = static_cast<pt_sock_t>(fd_);
    std::string buf;
    buf.reserve(256);
    char chunk[256];
    while (line_run_) {
        // Winsock recv() takes int for length and returns int; POSIX uses
        // size_t / ssize_t. Cast through pt_ssize_t to keep both compilers
        // happy without -Wnarrowing complaints.
        pt_ssize_t n = ::recv(fd, chunk,
                              static_cast<int>(sizeof(chunk)), 0);
        if (n <= 0) break;
        buf.append(chunk, chunk + n);

        std::size_t start = 0;
        for (std::size_t i = 0; i < buf.size(); ++i) {
            if (buf[i] == '\n') {
                auto line = buf.substr(start, i - start);
                if (!line.empty() && line.back() == '\r') line.pop_back();
                start = i + 1;
                if (line.empty()) continue;

                // Synchronously execute on the main-thread queue and reply.
                std::mutex m;
                std::condition_variable cv;
                bool done = false;
                ExecuteResult result;
                Console::Get().QueueExecute(line,
                    [&](const ExecuteResult& r) {
                        std::lock_guard lock(m);
                        result = r;
                        done = true;
                        cv.notify_one();
                    });
                {
                    std::unique_lock lock(m);
                    cv.wait_for(lock, std::chrono::seconds(2),
                                [&]{ return done; });
                }
                if (done) {
                    if (result.ok) {
                        if (!result.output.empty()) {
                            ::send(fd, result.output.data(),
                                   static_cast<int>(result.output.size()), 0);
                            if (result.output.back() != '\n') {
                                ::send(fd, "\n", 1, 0);
                            }
                        }
                    } else {
                        std::string msg = "error: " + result.error + "\n";
                        ::send(fd, msg.data(),
                               static_cast<int>(msg.size()), 0);
                    }
                }
            }
        }
        if (start > 0) buf.erase(0, start);
    }
    PT_SOCK_CLOSE(fd);
}

