double NowSeconds() {
    using clk = std::chrono::system_clock;
    auto d = clk::now().time_since_epoch();
    return std::chrono::duration_cast<std::chrono::duration<double>>(d).count();
}

const char* LevelName(pt::log::Level lv) {
    switch (lv) {
        case pt::log::Level::Info:  return "info";
        case pt::log::Level::Warn:  return "warn";
        case pt::log::Level::Error: return "error";
    }
    return "info";
}

void SendStaticHttp(mg_connection* conn, const char* mime,
                    const unsigned char* data, std::size_t size) {
    mg_response_header_start(conn, 200);
    mg_response_header_add(conn, "Content-Type", mime, -1);
    mg_response_header_add(conn, "Cache-Control", "no-cache", -1);
    auto len_str = std::to_string(size);
    mg_response_header_add(conn, "Content-Length", len_str.c_str(),
                           static_cast<int>(len_str.size()));
    mg_response_header_send(conn);
    mg_write(conn, data, size);
}

